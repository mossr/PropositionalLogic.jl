# Already built-in notation for set theory.

∪ # union (\cup)
∩ # intersection (\cap)

∈ # in (\in)
∉ # !in (\notin)
∋ # in (\ni)
∌ # !in (\nni)

⊆ # issubset (\subseteq)
⊊ # issubset(A,B) && A != B (\subsetneq)
⊈ # !issubset (\nsubseteq)

≡ # === (\equiv)
≢ # !== (\nequiv)
≠ # != (\ne)

≤ # <= (\le)
≥ # >= (\ge)

⊕ = ⊻ # xor (\xor or \veebar or \oplus)